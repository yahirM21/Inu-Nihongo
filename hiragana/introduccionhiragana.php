            <center><p>Diptongos</p></center>
            <p class="justificado">Los diptongos son utilizados en el japonés al igual que en el español, un diptongo es la mezcla de dos
               vocales una después de la otra que al pronunciarlas se genera un solo sonido, como en las palabras “Bien” o “Puerta”. Sin
                embargo en el japonés se utilizan las sílabas con el sonido de la tabla “<font color="red"><ruby>い<rt>i</rt><ruby></font>” mas los sonidos “<font color="red"><ruby>ゃ<rt>ya</rt></ruby></font>”,
                 “<font color="red"><ruby>ゅ<rt>yu</rt></ruby></font>” y “<font color="red"><ruby>ょ<rt>yo</rt></ruby></font>” para crearlos, hay que tener en cuenta que estas ultimas son letras mas pequeñas de lo normal ya que estarias escribiendo otra cosa.<p>
                 <p class="justificado">Por ejemplo el sonido de un gato: </p>
                 <p class="justificado"><ruby>に<font color="red">ゃ</font><rt>n<font color="red">ya</font></rt></ruby> En este caso estariamos escribiendo el sonido de un gato.</p>
                 <p class="justificado"><ruby>に<font color="red">や</font><rt>ni<font color="red">ya</font></rt></ruby> Mientras que este estariamos escribiendo una palabra completamente diferente.</p>
